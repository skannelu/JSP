package com.domain;

public class Ride {
	
	int id;
	int driverid;
	int travellerid;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getDriverid() {
		return driverid;
	}
	public void setDriverid(int driverid) {
		this.driverid = driverid;
	}
	public int getTravellerid() {
		return travellerid;
	}
	public void setTravellerid(int travellerid) {
		this.travellerid = travellerid;
	}
	

}
