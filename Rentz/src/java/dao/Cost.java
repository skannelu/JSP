public class Cost {
    int carcost,days,retlocation,liability,full;
    float tax,finalcost;

    public int getCarcost() {
        return carcost;
    }

    public void setCarcost(int carcost) {
        this.carcost = carcost;
    }

    public int getDays() {
        return days;
    }

    public void setDays(int days) {
        this.days = days;
    }

    public int getRetlocation() {
        return retlocation;
    }

    public void setRetlocation(int retlocation) {
        this.retlocation = retlocation;
    }

    public int getLiability() {
        return liability;
    }

    public void setLiability(int liability) {
        this.liability = liability;
    }

    public int getFull() {
        return full;
    }

    public void setFull(int full) {
        this.full = full;
    }

    public float getTax() {
        return tax;
    }

    public void setTax(float tax) {
        this.tax = tax;
    }

    public float getFinalcost() {
        return finalcost;
    }

    public void setFinalcost(float finalcost) {
        this.finalcost = finalcost;
    }

        
}
